# Дан целочисленный двумерный массив, размерности n х m. Найти
# количество элементов кратных 7.

n = 3
m = 4

a = [
[1,4,9,14],
[7,5,7,3],
[6,21,8,2]
]

result = 0
for i in range(n):
    for j in range(m):
        if a[i][j] % 7 == 0:
            result = result + 1

print(f"Количество элементов, кратных 7: {result}")

