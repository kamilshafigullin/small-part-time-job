# Есть трехмерный список. Например:
# а = [[[1, 2, 3], [4, 5, 6]],
# [[7, 8, 9], [9, 8, 7]],
# [[0, 1, 2], [-1, -2]]
# ]
# С помощью list comprehension необходимо сформировать новый
# одномерный список, состоящий из значений элементов списка a.
# Результат выведите на экран.


a = [[[1, 2, 3], [4, 5, 6]],
[[7, 8, 9], [9, 8, 7]],
[[0, 1, 2], [-1, -2]]
]

b = [z for x in a for y in x for z in y]
print(b)

